    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        PessoaJuridica novo = new PessoaJuridica();
        novo.setCnpj(txtCNPJ.getText());
        novo.setEndereco(txtRua.getText());
        novo.setNome(txtNome.getText());
        novo.setCidade(txtCidade.getText());
        novo.setNumero(txtNumero.getText());
        Contato novoContato = new Contato();
        novoContato.setEmail(txtEmail.getText());
        novoContato.setSenha(txtSenha.getText());
        novoContato.setTelefone(Integer.parseInt(txtTelefone.getText()));
        novo.setContato(novoContato);
        PJ.add(novo);
        System.out.println("Pessoa FÃ­sica cadastrada com sucesso!");
        System.out.println(PJ);
        //MANDAR PARA BANCO DE DADOS
        try {
            File arq = new File("PessoaJuridico.txt");
            BufferedWriter bf = new BufferedWriter(new FileWriter(arq, true));
            for (int i = 0; i < PJ.size(); i++) {
                bf.write(PJ.get(i).paraString());
                bf.newLine();
            }
            bf.flush();
            bf.close();
        } catch (Exception e) {
            e.printStackTrace();
            System.out.println("erro ao criar arquivo");
        }
        TelaGeral telaAll = new TelaGeral();
        telaAll.setVisible(true);
        setVisible(false);
    }//GEN-LAST:event_btCadastrarActionPerformed

